#!/usr/bin/env node
/**
 * Standalone verification of the VO mux pipeline.
 *
 * Steps:
 *   1. TTS every example narration via ElevenLabs (cached on disk).
 *   2. ffprobe each clip for real duration.
 *   3. Compute per-beat startMs as cumulative max(visualMs, clipMs).
 *   4. Render a black placeholder video of total length.
 *   5. Mux clips at offsets into a single MP4.
 *   6. Use ffmpeg silencedetect to find non-silent intervals in the muxed audio.
 *   7. Diff actual non-silent starts vs expected startMs.
 *
 * No browser, no Comfy. Pure pipeline check.
 */

require('dotenv').config();

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const { spawn, execFileSync } = require('child_process');

const REPO = path.resolve(__dirname, '..');
const CACHE_DIR = path.join(REPO, 'tts-cache');
const OUT_DIR = path.join(REPO, 'recordings');
fs.mkdirSync(CACHE_DIR, { recursive: true });
fs.mkdirSync(OUT_DIR, { recursive: true });

const API_KEY = process.env.ELEVENLABS_API_KEY;
const VOICE_ID = process.env.ELEVENLABS_VOICE_ID || '21m00Tcm4TlvDq8ikWAM';
const MODEL = process.env.ELEVENLABS_MODEL || 'eleven_turbo_v2_5';
if (!API_KEY) { console.error('Missing ELEVENLABS_API_KEY in .env'); process.exit(1); }

// Visual hold per beat — matches replay.js DLY (800ms) feel
const VISUAL_MS = 800;

// Example narrations (mirrors EXAMPLE_FALLBACK_NARRATIONS in index.html:2928-2945).
// Beat order = numeric node ids 1..7, then connection links in their declared order.
const NARRATIONS = [
  ["1", "First, we use the Checkpoint Loader Simple node to load our pre-trained Stable Diffusion model, which contains all the necessary weights for generating images."],
  ["2", "This CLIP Text Encode node processes our negative prompt, 'text, watermark', converting it into numerical conditioning. This tells the AI what elements we specifically want to avoid in our final image."],
  ["3", "Similarly, this CLIP Text Encode node processes our positive prompt, describing 'beautiful scenery' and a 'purple galaxy bottle'. This is the core instruction telling the AI what kind of image we want to create."],
  ["4", "The Empty Latent Image node creates a blank canvas of random noise, sized at 512x512 pixels. This serves as the initial starting point that the model will transform into our desired image."],
  ["5", "The KSampler is the main engine of our image generation; it takes all our inputs – the model, prompts, and initial noise – and iteratively denoises the image until it becomes our final output."],
  ["6", "After the KSampler finishes, the VAEDecode node takes the compressed latent image and converts it back into a viewable, high-resolution pixel image. This is how we can actually see and use the image generated by the model."],
  ["7", "Finally, the Save Image node takes our decoded image and saves it to your computer. You can specify the output folder or a file prefix here."],
  ["link_1_1_2_0", "We connect the CLIP model from the Checkpoint Loader to the first CLIP Text Encode node, allowing it to interpret your positive text prompt."],
  ["link_1_1_3_0", "Then, connect the same CLIP model to the second CLIP Text Encode node for processing your negative text prompt."],
  ["link_1_0_5_0", "Next, we connect the core Stable Diffusion model from the Checkpoint Loader to the KSampler, as this model will guide the image generation process."],
  ["link_4_0_5_3", "Connect the initial empty latent image to the KSampler, providing the starting canvas for the image generation process."],
  ["link_3_0_5_1", "We link the positive text conditioning from its CLIP Text Encode node to the KSampler's positive conditioning input."],
  ["link_2_0_5_2", "Likewise, the negative text conditioning is connected to the KSampler's negative conditioning input."],
  ["link_5_0_6_0", "The sampled latent image from the KSampler is then connected to the VAE Decode node to transform it back into a viewable image."],
  ["link_1_2_6_1", "We connect the VAE model from the Checkpoint Loader to the VAE Decode node, providing the necessary model to convert the latent image."],
  ["link_6_0_7_0", "Finally, the decoded image is sent to the Save Image node, allowing you to preview and save your generated output."],
];

function cacheKey(text) {
  return crypto.createHash('sha256')
    .update(`${VOICE_ID}|${MODEL}|${text}`)
    .digest('hex').slice(0, 16);
}

async function ttsOne(text) {
  const key = cacheKey(text);
  const file = path.join(CACHE_DIR, `${key}.mp3`);
  if (fs.existsSync(file) && fs.statSync(file).size > 0) return file;

  const url = `https://api.elevenlabs.io/v1/text-to-speech/${VOICE_ID}?output_format=mp3_44100_128`;
  const resp = await fetch(url, {
    method: 'POST',
    headers: { 'xi-api-key': API_KEY, 'Content-Type': 'application/json', 'accept': 'audio/mpeg' },
    body: JSON.stringify({ text, model_id: MODEL }),
  });
  if (!resp.ok) {
    const err = await resp.text();
    throw new Error(`ElevenLabs ${resp.status}: ${err.slice(0, 400)}`);
  }
  const buf = Buffer.from(await resp.arrayBuffer());
  fs.writeFileSync(file, buf);
  return file;
}

function probeDurationMs(file) {
  const out = execFileSync('ffprobe', [
    '-v', 'error', '-show_entries', 'format=duration',
    '-of', 'default=noprint_wrappers=1:nokey=1', file,
  ]).toString().trim();
  return Math.round(parseFloat(out) * 1000);
}

async function run() {
  console.log(`▶ TTS ${NARRATIONS.length} clips (cached in ${path.relative(REPO, CACHE_DIR)})`);
  const beats = [];
  for (let i = 0; i < NARRATIONS.length; i++) {
    const [id, text] = NARRATIONS[i];
    process.stdout.write(`  [${String(i+1).padStart(2,'0')}/${NARRATIONS.length}] ${id} ... `);
    const file = await ttsOne(text);
    const clipMs = probeDurationMs(file);
    const holdMs = Math.max(VISUAL_MS, clipMs);
    beats.push({ id, file, clipMs, holdMs });
    console.log(`${clipMs}ms (hold ${holdMs}ms)`);
  }

  // Cumulative startMs per beat
  let cur = 0;
  beats.forEach(b => { b.startMs = cur; cur += b.holdMs; });
  const totalMs = cur;
  console.log(`\n▶ Total timeline: ${totalMs}ms (${(totalMs/1000).toFixed(1)}s)`);

  // Render black video of total length, 24fps, 1280x720 — placeholder for the screen capture
  const videoFile = path.join(OUT_DIR, 'verify-black.mp4');
  console.log(`▶ Rendering placeholder video → ${path.relative(REPO, videoFile)}`);
  execFileSync('ffmpeg', [
    '-y', '-f', 'lavfi', '-i', `color=c=black:s=1280x720:r=24:d=${(totalMs/1000).toFixed(3)}`,
    '-c:v', 'libx264', '-preset', 'ultrafast', '-pix_fmt', 'yuv420p', videoFile,
  ], { stdio: ['ignore', 'ignore', 'pipe'] });

  // Build the ffmpeg mux command. amix ignores -itsoffset (it aligns inputs at t=0),
  // so we use adelay per-input to shift each clip onto the output timeline, then amix.
  const args = ['-y', '-i', videoFile];
  beats.forEach(b => { args.push('-i', b.file); });

  const N = beats.length;
  // For each audio input: pad each channel by startMs of silence with adelay, then label.
  // "all=1" applies the delay to every channel (mono → stereo safe).
  const delayChain = beats.map((b, i) =>
    `[${i+1}:a]adelay=${b.startMs}|${b.startMs}:all=1[a${i}]`
  ).join(';');
  const mixInputs = beats.map((_, i) => `[a${i}]`).join('');
  // duration=first → use video duration (input 0). normalize=0 keeps clip levels intact.
  const filter = `${delayChain};${mixInputs}amix=inputs=${N}:duration=longest:normalize=0[aout]`;
  args.push('-filter_complex', filter);
  args.push('-map', '0:v', '-map', '[aout]');
  args.push('-c:v', 'copy', '-c:a', 'aac', '-b:a', '192k');
  const outFile = path.join(OUT_DIR, 'verify-muxed.mp4');
  args.push(outFile);

  console.log(`▶ Muxing → ${path.relative(REPO, outFile)}`);
  execFileSync('ffmpeg', args, { stdio: ['ignore', 'ignore', 'pipe'] });

  // ───────────────────────────────────────────────────────────────────────────
  // CHECK A: total output duration vs sum(holdMs)
  // ───────────────────────────────────────────────────────────────────────────
  const probedTotalMs = probeDurationMs(outFile);
  const totalDelta = probedTotalMs - totalMs;
  console.log(`\n=== CHECK A: total duration ===`);
  console.log(`expected: ${totalMs}ms   actual: ${probedTotalMs}ms   delta: ${totalDelta}ms`);
  const checkA = Math.abs(totalDelta) <= 100;
  console.log(`${checkA ? '✓ PASS' : '✗ FAIL'} (tolerance ±100ms)`);

  // ───────────────────────────────────────────────────────────────────────────
  // CHECK B: onset drift via silencedetect — find actual audio onsets,
  // pair to nearest expected startMs, report max drift
  // ───────────────────────────────────────────────────────────────────────────
  console.log(`\n=== CHECK B: per-beat onset drift ===`);
  const sdOut = (() => {
    const { spawnSync } = require('child_process');
    const r = spawnSync('ffmpeg', [
      '-v', 'info', '-i', outFile,
      '-af', 'silencedetect=noise=-40dB:d=0.20',
      '-f', 'null', '-',
    ]);
    return (r.stderr || '').toString();
  })();
  // silence_end = the moment audio resumes after a silent gap
  const silenceEnds = [...sdOut.matchAll(/silence_end:\s*([\d.]+)/g)]
    .map(m => Math.round(parseFloat(m[1]) * 1000));
  // Beat 0 starts at 0ms (no silence preceding); for beats 1..N look for an onset
  // near each expected startMs. With holdMs=clipMs, gaps between beats may be <200ms
  // and silencedetect may not register every one; we tolerate that and only fail on
  // a clear drift > tolerance for onsets we DO detect.
  console.log(`detected ${silenceEnds.length} onset(s) in muxed audio`);
  console.log(`\n${'beat'.padEnd(20)} ${'expectMs'.padStart(10)} ${'nearOnset'.padStart(11)} ${'drift'.padStart(8)}  status`);
  console.log('-'.repeat(64));
  const DRIFT_TOL = 200; // ms
  let maxDrift = 0, checkB = true, matched = 0;
  beats.forEach((b, i) => {
    if (i === 0) {
      console.log(`${b.id.padEnd(20)} ${String(b.startMs).padStart(10)} ${'(start)'.padStart(11)} ${'—'.padStart(8)}  ✓`);
      return;
    }
    let nearest = null, dist = Infinity;
    for (const e of silenceEnds) {
      const d = Math.abs(e - b.startMs);
      if (d < dist) { dist = d; nearest = e; }
    }
    // Only count as "matched" if within 500ms (otherwise this onset is for a different beat)
    if (nearest != null && dist <= 500) {
      matched++;
      if (dist > maxDrift) maxDrift = dist;
      const ok = dist <= DRIFT_TOL;
      if (!ok) checkB = false;
      const drift = nearest - b.startMs;
      console.log(`${b.id.padEnd(20)} ${String(b.startMs).padStart(10)} ${String(nearest).padStart(11)} ${(drift>=0?'+':'')+drift.toString().padStart(7)}  ${ok ? '✓' : `✗ drift ${dist}ms`}`);
    } else {
      // No onset detected near this beat — could mean clips are touching with no gap.
      // Not a failure on its own, just noted.
      console.log(`${b.id.padEnd(20)} ${String(b.startMs).padStart(10)} ${'(no gap)'.padStart(11)} ${'—'.padStart(8)}  · seamless`);
    }
  });
  console.log('-'.repeat(64));
  console.log(`onsets matched: ${matched}, max drift among matched: ${maxDrift}ms (tolerance ${DRIFT_TOL}ms)`);
  console.log(`${checkB ? '✓ PASS' : '✗ FAIL'}`);

  // ───────────────────────────────────────────────────────────────────────────
  // CHECK C: mid-clip play-through — sample at startMs + clipMs/2
  // ───────────────────────────────────────────────────────────────────────────
  console.log(`\n=== CHECK C: mid-clip play-through ===`);
  // If audio is present, RMS_level will be well above the silence floor (~-90dB).
  // Pass criterion: RMS_level >= -45 dB (clearly audible speech).
  console.log(`\n▶ Sampling RMS at each beat (300ms window, threshold -45 dB)`);

  function rmsAt(file, startMs, durMs) {
    // -ss before -i = fast seek; astats prints "RMS level dB: <n>" to stderr.
    const args = [
      '-v', 'info',
      '-ss', (startMs/1000).toFixed(3),
      '-t', (durMs/1000).toFixed(3),
      '-i', file,
      '-vn',
      '-af', 'astats=reset=1',
      '-f', 'null', '-',
    ];
    let stderr = '';
    try {
      execFileSync('ffmpeg', args, { stdio: ['ignore', 'ignore', 'pipe'] });
    } catch (e) {
      stderr = (e.stderr || '').toString();
    }
    if (!stderr) {
      // success path — capture stderr by re-running with spawnSync if needed
      const { spawnSync } = require('child_process');
      const r = spawnSync('ffmpeg', args);
      stderr = (r.stderr || '').toString();
    }
    // Prefer "Overall" section's RMS level — astats prints per-channel then Overall.
    // We just take the LAST "RMS level dB" line, which is Overall.
    const matches = [...stderr.matchAll(/RMS level dB:\s*(-?\d+\.?\d*|-?inf|nan)/g)];
    if (!matches.length) return null;
    const last = matches[matches.length - 1][1];
    if (last === '-inf' || last === 'inf' || last === 'nan') return -Infinity;
    return parseFloat(last);
  }

  // Sample at 25%, 50%, 75% of each clip — require AT LEAST ONE audible to
  // confirm playthrough. Single-window sampling can land on a between-word pause
  // in the TTS speech (false negative); requiring any-of-three is robust to that.
  const THRESHOLD_DB = -45;
  console.log(`${'beat'.padEnd(20)} ${'@25%'.padStart(8)} ${'@50%'.padStart(8)} ${'@75%'.padStart(8)}  status`);
  console.log('-'.repeat(60));
  let cPass = 0, cFail = 0;
  beats.forEach(b => {
    const samples = [0.25, 0.5, 0.75].map(frac => {
      const t = b.startMs + Math.floor(b.clipMs * frac);
      return rmsAt(outFile, t, 200);
    });
    const heard = samples.some(rms => rms != null && rms >= THRESHOLD_DB);
    if (heard) cPass++; else cFail++;
    const fmt = r => r == null ? 'n/a' : r === -Infinity ? '-inf' : r.toFixed(1);
    console.log(`${b.id.padEnd(20)} ${fmt(samples[0]).padStart(8)} ${fmt(samples[1]).padStart(8)} ${fmt(samples[2]).padStart(8)}  ${heard ? '✓' : '✗ all silent — clip likely truncated'}`);
  });
  console.log('-'.repeat(60));
  const checkC = cFail === 0;
  console.log(`${checkC ? '✓ PASS' : '✗ FAIL'} (${cPass}/${beats.length} clips heard at ≥1 of 3 sample points)`);

  // ───────────────────────────────────────────────────────────────────────────
  // CHECK D: boundary continuity — sample just before each beat boundary
  // (i.e. tail of previous clip). Should be audible (no premature cut).
  // ───────────────────────────────────────────────────────────────────────────
  console.log(`\n=== CHECK D: boundary continuity (previous clip's tail) ===`);
  console.log(`${'before-beat'.padEnd(20)} ${'sampleMs'.padStart(10)} ${'rmsDb'.padStart(10)}  status`);
  console.log('-'.repeat(60));
  let dPass = 0, dFail = 0;
  beats.forEach((b, i) => {
    if (i === 0) return; // no previous clip
    const sampleMs = b.startMs - 150; // 150ms into previous clip's tail
    const rms = rmsAt(outFile, sampleMs, 100);
    const heard = rms != null && rms >= THRESHOLD_DB;
    if (heard) dPass++; else dFail++;
    const rmsStr = rms == null ? 'n/a' : (rms === -Infinity ? '-inf' : rms.toFixed(1));
    console.log(`${b.id.padEnd(20)} ${String(sampleMs).padStart(10)} ${rmsStr.padStart(10)}  ${heard ? '✓' : '· tail-silence (likely natural)'}`);
  });
  console.log('-'.repeat(60));
  // Note: not a hard failure — TTS clips often have ~100-300ms of natural trailing silence.
  // We just report it so user can spot if clips are getting cut off.
  console.log(`${dPass}/${beats.length - 1} boundaries had audible tail. (Failures here usually = TTS natural trailing silence, not truncation.)`);

  // Sanity: post-timeline should be silent
  const afterMs = totalMs + 500;
  const afterRms = rmsAt(outFile, afterMs, 300);
  const checkSanity = afterRms == null || afterRms < THRESHOLD_DB;
  console.log(`\nsanity (post-timeline ${afterMs}ms): ${afterRms == null ? 'n/a' : (afterRms === -Infinity ? '-inf' : afterRms.toFixed(1))} dB → ${checkSanity ? '✓ silent as expected' : '✗ unexpected audio'}`);

  // ───────────────────────────────────────────────────────────────────────────
  // SUMMARY
  // ───────────────────────────────────────────────────────────────────────────
  console.log('\n' + '='.repeat(60));
  console.log('SUMMARY');
  console.log('='.repeat(60));
  console.log(`  A) total duration       ${checkA ? '✓' : '✗'}   (Δ ${totalDelta}ms)`);
  console.log(`  B) onset drift          ${checkB ? '✓' : '✗'}   (max ${maxDrift}ms across ${matched} matched)`);
  console.log(`  C) mid-clip playthrough ${checkC ? '✓' : '✗'}   (${cPass}/${beats.length})`);
  console.log(`  D) boundary continuity      ·   (${dPass}/${beats.length - 1} audible tails — info only)`);
  console.log(`  sanity (post silence)   ${checkSanity ? '✓' : '✗'}`);
  const overall = checkA && checkB && checkC && checkSanity;
  console.log(`\n${overall ? '✅ ALL HARD CHECKS PASS' : '❌ ONE OR MORE HARD CHECKS FAILED'}`);

  let pass = overall ? 1 : 0;
  let fail = overall ? 0 : 1;
  console.log(`\nOutput: ${outFile}`);
  console.log(`Open with: open "${outFile}"`);
  process.exit(fail === 0 ? 0 : 2);
}

run().catch(e => { console.error('FATAL:', e); process.exit(1); });
